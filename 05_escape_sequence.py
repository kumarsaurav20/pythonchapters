# Escape sequence character.

a = "Saurav is a boy \nHe is a very good guy"
print(a) #\n for a new line 

b = "Saurav  is a good boy. \nHe is \tvery good guy"
print(b) #\t used for space tab between character.

c ="Saurav  is a good boy. \nHe is \tvery good guy\\"
print(c) #\\ used for back slash 
d = "Saurav  is a good boy. \nHe is \tvery good guy\' "
print(d) #\' is used for single quote in string .
